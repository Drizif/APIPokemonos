const { Schema, model } = require('mongoose');

const TrainerSchema = Schema({
  noCtrl: {
    type: String,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true,
  },
  favPok: {
    type: Array,
    required: false
  }
});

TrainerSchema.method('toJSON', function () {
  const { __v, _id, ...object } = this.toObject();

  object.id = _id;
  return object;
});

module.exports = model('Trainer', TrainerSchema);